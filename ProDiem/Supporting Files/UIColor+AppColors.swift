import Foundation
import UIKit

extension UIColor {
    class func appDarkGreen() -> UIColor {
        return UIColor(red: 28/255, green: 49/255, blue: 71/255, alpha: 1.0)
    }
    
    class func appLightGreen() -> UIColor {
        return UIColor(red: 36/255, green: 59/255, blue: 83/255, alpha: 1.0)
    }
    
    class func appYellowColor() -> UIColor {
        return UIColor(red: 237/255, green: 186/255, blue: 88/255, alpha: 1.0)
    }
    
    class func appRedColor() -> UIColor {
        return UIColor(red: 168/255, green: 63/255, blue: 85/255, alpha: 1.0)
    }
    
    class func appOrangeColor() -> UIColor {
        return UIColor(red: 259/255, green: 149/255, blue: 0/255, alpha: 1.0)
    }
    
    class func appOrangeColorFade() -> UIColor {
        return UIColor(red: 259/255, green: 149/255, blue: 0/255, alpha: 0.25)
    }
    
    class func appCyanColor() -> UIColor {
        return UIColor(red: 26/255, green: 214/255, blue: 253/255, alpha: 1.0)
    }
    
    class func appLimeGreenColor() -> UIColor {
        return UIColor(red: 76/255, green: 217/255, blue: 100/255, alpha: 1.0)
    }
    
    class func appLimeGreenColorFade() -> UIColor {
        return UIColor(red: 76/255, green: 217/255, blue: 100/255, alpha: 0.25)
    }

    class func appLightGray() -> UIColor {
        return UIColor(red: 255/255, green: 255/255, blue: 255/255, alpha: 1.0)
    }


}
